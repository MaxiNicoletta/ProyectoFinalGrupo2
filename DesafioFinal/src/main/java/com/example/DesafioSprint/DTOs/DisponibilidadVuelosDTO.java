import java.util.Date;

@Data
@AllArgsConstructor
public class DisponibilidadVuelosDTO {
    @JsonFormat(pattern = "dd/MM/yyyy", timezone = "GMT-3")
    private Date dateFrom;
    @JsonFormat(pattern = "dd/MM/yyyy", timezone = "GMT-3")
    private Date dateTo;
    private String origin;
    private String destination;
}
